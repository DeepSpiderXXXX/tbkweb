package com.limon.base.model;

public class TbkType {
	private Integer id;
	private String typename;
	private String taobaoType;
	private Integer sort;
	private Double yj;
	private Double srbl;
	private Double yhbl;
	private Integer salenum;
	private Integer count;
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getTypename() {
		return typename;
	}
	public void setTypename(String typename) {
		this.typename = typename;
	}
	public String getTaobaoType() {
		return taobaoType;
	}
	public void setTaobaoType(String taobaoType) {
		this.taobaoType = taobaoType;
	}
	public Integer getSort() {
		return sort;
	}
	public void setSort(Integer sort) {
		this.sort = sort;
	}
	public Double getYj() {
		return yj;
	}
	public void setYj(Double yj) {
		this.yj = yj;
	}
	public Double getSrbl() {
		return srbl;
	}
	public void setSrbl(Double srbl) {
		this.srbl = srbl;
	}
	public Double getYhbl() {
		return yhbl;
	}
	public void setYhbl(Double yhbl) {
		this.yhbl = yhbl;
	}
	public Integer getSalenum() {
		return salenum;
	}
	public void setSalenum(Integer salenum) {
		this.salenum = salenum;
	}
}
