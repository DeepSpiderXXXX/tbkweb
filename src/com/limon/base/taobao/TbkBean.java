package com.limon.base.taobao;

public class TbkBean {
	private String ret;
	private String url;
	private String content;
	private String picUrl;
	private String taopwdOwnerId;
	private String validDate;
	public String getRet() {
		return ret;
	}
	public void setRet(String ret) {
		this.ret = ret;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getPicUrl() {
		return picUrl;
	}
	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}
	public String getTaopwdOwnerId() {
		return taopwdOwnerId;
	}
	public void setTaopwdOwnerId(String taopwdOwnerId) {
		this.taopwdOwnerId = taopwdOwnerId;
	}
	public String getValidDate() {
		return validDate;
	}
	public void setValidDate(String validDate) {
		this.validDate = validDate;
	}
}
